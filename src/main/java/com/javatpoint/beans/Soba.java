package com.javatpoint.beans;



public class Soba {

	public int SobaId;
	public double skoda;
	public String opis;
	
	
	public int getSobaId() {
		return SobaId;
	}

	public void setSobaId(int sobaId) {
		SobaId = sobaId;
	}

	public double getSkoda() {
		return skoda;
	}

	public void setSkoda(double skoda) {
		this.skoda = skoda;
	}

	public String getOpis() {
		return opis;
	}

	public void setOpis(String opis) {
		this.opis = opis;
	}	
	
}